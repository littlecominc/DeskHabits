'use client';

import { useState } from 'react';
import { createClient } from '@/lib/supabase/client';

export default function OAuthButtons() {
  const supabase = createClient();
  const [loading, setLoading] = useState<'google' | 'apple' | null>(null);

  async function signInWith(provider: 'google' | 'apple') {
    setLoading(provider);
    const siteUrl = process.env.NEXT_PUBLIC_SITE_URL ?? window.location.origin;
    await supabase.auth.signInWithOAuth({
      provider,
      options: { redirectTo: `${siteUrl}/auth/callback` },
    });
  }

  return (
    <div className="flex flex-col gap-3">
      <button
        onClick={() => signInWith('google')}
        disabled={loading !== null}
        className="flex items-center justify-center gap-3 rounded-xl2 border border-border bg-panel py-4 text-sm font-semibold disabled:opacity-50"
      >
        <GoogleIcon />
        {loading === 'google' ? 'Redirecting…' : 'Continue with Google'}
      </button>
      <button
        onClick={() => signInWith('apple')}
        disabled={loading !== null}
        className="flex items-center justify-center gap-3 rounded-xl2 border border-border bg-panel py-4 text-sm font-semibold disabled:opacity-50"
      >
        <AppleIcon />
        {loading === 'apple' ? 'Redirecting…' : 'Continue with Apple'}
      </button>
    </div>
  );
}

function GoogleIcon() {
  return (
    <svg width="18" height="18" viewBox="0 0 18 18" xmlns="http://www.w3.org/2000/svg">
      <path fill="#4285F4" d="M17.64 9.2c0-.64-.06-1.25-.16-1.84H9v3.48h4.84a4.14 4.14 0 0 1-1.8 2.71v2.26h2.92c1.7-1.57 2.68-3.88 2.68-6.61z" />
      <path fill="#34A853" d="M9 18c2.43 0 4.47-.8 5.96-2.18l-2.92-2.26c-.81.54-1.84.86-3.04.86-2.34 0-4.32-1.58-5.03-3.7H.96v2.33A9 9 0 0 0 9 18z" />
      <path fill="#FBBC05" d="M3.97 10.72A5.4 5.4 0 0 1 3.69 9c0-.6.1-1.18.28-1.72V4.95H.96A9 9 0 0 0 0 9c0 1.45.35 2.83.96 4.05l3.01-2.33z" />
      <path fill="#EA4335" d="M9 3.58c1.32 0 2.51.46 3.44 1.35l2.59-2.59C13.46.89 11.43 0 9 0A9 9 0 0 0 .96 4.95l3.01 2.33C4.68 5.16 6.66 3.58 9 3.58z" />
    </svg>
  );
}

